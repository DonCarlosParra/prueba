    $(document).ready(function() {
        let dropdownLink = document.querySelectorAll(".nav-link.dropdown-toggle");

        dropdownLink.forEach(function(element) {
            let dropdownMenu = element.nextElementSibling;

            element.addEventListener("mouseover", function() {
                dropdownMenu.classList.add("show");
                this.parentNode.classList.add("show");
            });

            element.addEventListener("mouseout", function() {
                dropdownMenu.classList.remove("show");
                this.parentNode.classList.remove("show");
            });

            dropdownMenu.addEventListener("mouseover", function() {
                this.classList.add("show");
                this.parentNode.classList.add("show");
            });

            dropdownMenu.addEventListener("mouseout", function() {
                this.classList.remove("show");
                this.parentNode.classList.remove("show");
            });
        });

        $('#carouselE1').carousel();

        // Avanzar automáticamente cada 3 segundos
        var intervalId = setInterval(function() {
            $('#carouselE1').carousel('next');
        }, 6000); // 3000 milisegundos = 3 segundos


        $('#carouselE1').on('slid.bs.carousel', function() {
            var currentIndex = $('#carouselE1 .carousel-item.active').index();
            var totalItems = $('#carouselE1 .carousel-item').length;

            if (currentIndex + 1 >= totalItems) {
                clearInterval(intervalId); // Detener el intervalo actual
                setTimeout(function() {
                    $('#carouselE1').carousel(0); // Reiniciar el carrusel al primer elemento
                    intervalId = setInterval(function() {
                        $('#carouselE1').carousel('next');
                    }, 6000); // Reiniciar el intervalo después de 3 segundos
                }, 4000); // Esperar 1 segundo antes de reiniciar el carrusel
            }
        });

    });
    window.addEventListener('scroll', function() {
    var navbar = document.getElementById('navbar');
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
});
